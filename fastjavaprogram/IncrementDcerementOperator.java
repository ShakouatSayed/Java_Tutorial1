public class IncrementDcerementOperator {
    public static void main(String[] args) {
        int a = 6, b = 12;
        a++;
        b++;
        System.out.println("Print the increment value a is: " + a);
        System.out.println("Print the increment value b is: " + b);
        a--;
        b--;
        System.out.println("Print the decrement value a is: " + a);
        System.out.println("Print the decrement value b is: " + b);
    }
}

package problem_solving_question;

public class EnhanceLoop {
    public static void main(String[] args) {

        int primes[] = { 2, 3, 7, 13 };
        for (int i : primes)
            System.out.print(i + " ");
        System.out.println("\n");
        String str[] = { "C", "C++", "JAVA", "Perl", "Kotlin", "Python", "Ruby", "GO" };
        for (String str1 : str)
            System.out.print(str1+" ");
    }

}

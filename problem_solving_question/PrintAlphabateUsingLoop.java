package problem_solving_question;

/**
 * PrintAlphabateUsingLoop
 */
public class PrintAlphabateUsingLoop {

    public static void main(String[] args) {
        char c1 = 'A', c2 = 'Z';
        for(char i = c1; i <= c2; i++){
            System.out.print(i + " ");
        }        
    }
}